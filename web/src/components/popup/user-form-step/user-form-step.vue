<template>
  <div
      class="loader"
      v-if="loading"
  >Loading...</div>
  <form
      class="land-user-form-step"
      @submit.prevent="submitForm"
      v-else
  >
    <h3 class="typo-h3">
      Мы проверяем информацию, которую Вы предоставили.
      <br />
      <br />
      Введите Ваши контакты и мы сообщим результат
    </h3>
    <input
        class="input land-user-form-step__input"
        v-model="userPhone"
        type="tel"
        placeholder="Введите Ваш номер телефона"
    >
    <input
        class="input land-user-form-step__input"
        v-model="userName"
        type="text"
        placeholder="Введите Ваше имя"
    >
    <input
        class="input land-user-form-step__input"
        v-model="userEmail"
        type="email"
        placeholder="Введите Ваш email"
    >
    <button
        type="submit"
        class="btn-default btn-primary"
    >
      Вернуть средства
    </button>
  </form>
</template>

<script>
export default {
  name: "user-form-step",
  data: () => ({
    userPhone: '',
    userName: '',
    userEmail: '',
    loading: true,
  }),
  methods: {
    submitForm() {
      if (this.userPhone && this.userName && this.userEmail) {
        this.$emit('submit', {
          phone: this.userPhone,
          name: this.userName,
          email: this.userEmail,
        });
      }
    }
  },
  created() {
    setTimeout(() => {
      this.loading = false;
    }, 3000);
  }
}
</script>

